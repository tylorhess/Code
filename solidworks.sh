# solid white workspace background color (no gray gradient)
	Tools > Options > Colors > Viewport Background   > 255, 255, 255
	Tools > Options > Colors > Top Gradient Color    > 255, 255, 255
	Tools > Options > Colors > Bottom Gradient Color > 255, 255, 255
	Tools > Options > Colors > choose "Plain (Viewport Background color above)"

# move assembly to origin
	(in assembly) > Move Component > Move (header) > change "Free Drag" to "To XYZ Position" > 0, 0, 0



